FEATURES = {
    "small_grass": (8, 6, 3, 3),
    "large_grass": (18, 12, 7, 7),
    "high_grass": (22, 6, 6, 6),
    "pond": (12, 8, 3, 2),
    "house": (20, 4, 2, 2),
    "path": None,
}


def apply_map_features():
    grid = [row[:] for row in ISLAND_MAP]
    sx, sy, sw, sh = FEATURES["small_grass"]
    for y in range(sy, sy + sh):
        for x in range(sx, sx + sw):
            if grid[y][x] == TILE_SAND:
                grid[y][x] = TILE_GRASS_SHORT

    lx, ly, lw, lh = FEATURES["large_grass"]
    for y in range(ly, ly + lh):
        for x in range(lx, lx + lw):
            if grid[y][x] == TILE_SAND:
                grid[y][x] = TILE_GRASS_TALL

    hx, hy, hw, hh = FEATURES["high_grass"]
    for y in range(hy, hy + hh):
        for x in range(hx, hx + hw):
            if grid[y][x] == TILE_SAND:
                grid[y][x] = TILE_GRASS_ELITE

    px, py, pw, ph = FEATURES["pond"]
    for y in range(py, py + ph):
        for x in range(px, px + pw):
            if grid[y][x] == TILE_SAND:
                grid[y][x] = TILE_POND

    hx, hy, hw, hh = FEATURES["house"]
    for y in range(hy, hy + hh):
        for x in range(hx, hx + hw):
            if grid[y][x] == TILE_SAND:
                grid[y][x] = TILE_HOUSE

    for y in range(MAP_HEIGHT):
        for x in range(MAP_WIDTH):
            if grid[y][x] == TILE_SAND and (x + y) % 5 == 0:
                grid[y][x] = TILE_PATH

    return grid


ISLAND_MAP = apply_map_features()

